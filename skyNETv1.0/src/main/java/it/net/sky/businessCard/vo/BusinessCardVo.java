package it.net.sky.businessCard.vo;

public class BusinessCardVo {
	public int business_seq;
	public String business_Name;
	public String business_Co;
	public String business_Tel;
	public String business_Rank;
	public String business_RegId;
	public int getBusiness_seq() {
		return business_seq;
	}
	public void setBusiness_seq(int business_seq) {
		this.business_seq = business_seq;
	}
	public String getBusiness_Name() {
		return business_Name;
	}
	public void setBusiness_Name(String business_Name) {
		this.business_Name = business_Name;
	}
	public String getBusiness_Co() {
		return business_Co;
	}
	public void setBusiness_Co(String business_Co) {
		this.business_Co = business_Co;
	}
	public String getBusiness_Tel() {
		return business_Tel;
	}
	public void setBusiness_Tel(String business_Tel) {
		this.business_Tel = business_Tel;
	}
	public String getBusiness_Rank() {
		return business_Rank;
	}
	public void setBusiness_Rank(String business_Rank) {
		this.business_Rank = business_Rank;
	}
	public String getBusiness_RegId() {
		return business_RegId;
	}
	public void setBusiness_RegId(String business_RegId) {
		this.business_RegId = business_RegId;
	}

}
